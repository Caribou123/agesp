#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	int i;
	char *save;

	i = 0;
	save = s;
	if (n == 0)
		return ;
	while (save[i] && i < n)
	{
		save[i] = 0;
		i++;
	}
	s = save;
}
