#include "libft.h"

int	ft_strcmp(const char *s1, const char *s2)
{
	int i;
	int save_s1;

	i = 0;
	save_s1 = 0;
	while (s1[i] == s2[i] && (s1[i] != '\0' && s2[i] != '\0'))
	{
		i++;
	}
	if (s1[i] != s2[i])
	{
		save_s1 = s1[i] - s2[i];
	}
	return (save_s1);
}
