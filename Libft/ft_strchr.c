#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int i;
	char *z;

	i = 0;
	z = "";
	if (c == '\0')
	{
		while (s[i])
			i++;
		return (char*)(&s[i]);
	}
	if (s[i] == '\0')
		return (0);
	while (s[i] && s[i] != c)
	{
		i++;
	}
	if (s[i] == '\0')
		return (0);
	return (char*)(&s[i]);
}
