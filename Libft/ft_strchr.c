#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int i;
	char *z;

	i = 0;
	z = "";
	if (c < 1)
	{
		return (z);
	}
	while (s[i])
	{
		if (s[i] == (char)(c))
			return (char*)&s[i];
		i++;
	}
	return (0);
}
