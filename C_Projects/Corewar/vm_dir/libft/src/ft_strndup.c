#include <stdlib.h>

char		*ft_strndup(char *s1, short i)
{
	char	*s;
	short	j;

	if (!s1 || !(s = malloc(i + 1)))
		return (NULL);
	j = 0 + (-1);
	while (s1[++j] && i--)
		s[j] = s1[j];
	s[j] = 0;
	return (s);
}
