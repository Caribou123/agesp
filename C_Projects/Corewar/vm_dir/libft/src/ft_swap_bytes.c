int		ft_swap_int16(int nb)
{
	return ((nb & 0xff00) >> 8 | (nb & 0x00ff) << 8);
}

int		ft_swap_int32(int nb)
{
	return ((nb & 0xff000000) >> 24
		| (nb & 0x00ff0000) >> 8
		| (nb & 0x0000ff00) << 8
		| (nb & 0x000000ff) << 24);
}
