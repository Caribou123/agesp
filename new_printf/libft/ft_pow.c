float	ft_pow(float nb, int pow)
{
	if (pow > 1)
	{
		return (nb * ft_pow(nb, pow - 1));
	}
	else if (pow < 0)
	{
		return (1 / ft_pow(nb, -pow));
	}
	if (pow == 1)
		return (nb);
	return (0);
}
