#include "libft.h"

int	ft_sqrt(int num)
{
	int n;
	int n1;

	if (0 == num)
		return (0);
	n = (num / 2) + 1;
	n1 = (n + (num / n)) / 2;
	while (n1 < n)
	{
		n = n1;
		n1 = (n + (num / n)) / 2;
	}
	return (n);
}
